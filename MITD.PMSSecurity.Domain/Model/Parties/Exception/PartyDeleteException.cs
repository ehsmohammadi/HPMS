using MITD.Core;

namespace MITD.PMSSecurity.Domain
{
    public class PartyDeleteException : PartyException, IDeleteException
    {
        public PartyDeleteException(string domainObjectName, string relatedObjectName)
            : base("can not delete, " + domainObjectName + "that has related data with" + relatedObjectName)
        {
            DomainObjectName = domainObjectName;
            RelatedObjectName = relatedObjectName;
        }

        public string DomainObjectName { get; private set; }
        public string RelatedObjectName { get; private set; }
    }
}
